export const getOrgEntity = (
  name: string
): {
  name: string;
  latex: string;
  requireLatexMath: boolean;
  html: string;
  ascii: string;
  latin1: string;
  utf8: string;
} | null => {
  const e = orgEntities.find((x) => x[0] === name);
  if (!e) return null;

  return {
    name,
    latex: e[1],
    requireLatexMath: e[2],
    html: e[3],
    ascii: e[4],
    latin1: e[5],
    utf8: e[6],
  };
};

